    private void generarArbolNumActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generarArbolNumActionPerformed
        try {
            this.panel.setText("");
            String order = this.orden2.getText();
            if (!this.orden2.getText().equals("") && Integer.parseInt(this.orden2.getText()) > 2) {
                if (radioBotonNumero.isSelected()) {
                    arbolConNumeroNumero(order);
                } else if (radioBotonCaracter.isSelected()) {
                    arbolConLetraLetra(order);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Ingrese el orden (>2) | numero/caracter!!", "MENSAJE", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_generarArbolNumActionPerformed

    private void generarArbolListaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generarArbolListaActionPerformed
        try {
            this.panel.setText("");
            String order = this.orden.getText();
            String list = this.lista.getText().trim();
            if (!this.orden.getText().equals("") && !this.lista.getText().equals("") && Integer.parseInt(this.orden.getText()) > 2) {
                if (radioBotonNumero.isSelected()) {
                    arbolConListaNumeros(order, list);
                } else if (radioBotonCaracter.isSelected()) {
                    arbolConListaLetras(order, list);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Ingrese el orden (>2) | lista!!", "MENSAJE", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_generarArbolListaActionPerformed

    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        if (!this.num.getText().equals("")) {
            if (radioBotonNumero.isSelected()) {
                numeroNumero.add(Integer.parseInt(num.getText()));
                num.setText("");
            } else if (radioBotonCaracter.isSelected()) {
                letraLetra.add(num.getText());
                num.setText("");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe ingresae numero/caracter!!", "MENSAJE", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_addActionPerformed

    private void borrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_borrarActionPerformed
        if (radioBotonNumero.isSelected()) {
            numeroNumero = new ArrayList();
            this.num.setText("");
            this.orden2.setText("");
            this.panel.setText("");
        } else if (radioBotonCaracter.isSelected()) {
            letraLetra = new ArrayList();
            num.setText("");
            this.orden2.setText("");
            this.panel.setText("");
        }
    }//GEN-LAST:event_borrarActionPerformed

    private void arbolConNumeroNumero(String ordenArbol) {
        treeNumber = Arbol.getArbol(Integer.parseInt(ordenArbol), new ComparadorEnteros());
        for (Integer i : numeroNumero) {
            System.out.println("\n\n\tITERACION ");
            System.out.println("Num: " + i);
            treeNumber.agregar(i);
            System.out.println(treeNumber);
            this.panel.append("\n----------------------------------------------------\n");
            this.panel.append("  Numero insertado : " + i + "\n");
            this.panel.append(treeNumber.toString());
        }
    }

    private void arbolConLetraLetra(String ordenArbol) {
        treeString = Arbol.getArbol(Integer.parseInt(ordenArbol), new ComparadorString());
        for (String i : letraLetra) {
            System.out.println("\n\n\tITERACION ");
            System.out.println("Letra: " + i);
            treeString.agregar(i);
            System.out.println(treeString);
            this.panel.append("\n----------------------------------------------------\n");
            this.panel.append("  Letra insertada insertado : " + i + "\n");
            this.panel.append(treeString.toString());
        }
    }

    private void arbolConListaNumeros(String ordenArbol, String listanumeros) {
        listNumeros = new ArrayList();
        String[] listaNumeros = listanumeros.split(",");
        for (int i = 0; i < listaNumeros.length; i++) { // no recuerdo si es length o length()
            listNumeros.add(Integer.parseInt(listaNumeros[i]));
        }
        treeNumber = Arbol.getArbol(Integer.parseInt(ordenArbol), new ComparadorEnteros());
        for (Integer i : listNumeros) {
            treeNumber.agregar(i);
        }
        System.out.println(treeNumber);
        this.orden.setText("");
        this.lista.setText("");
        this.panel.setText("");
        this.panel.append("\n");
        this.panel.append(treeNumber.toString());
    }

    private void arbolConListaLetras(String ordenArbol, String listaLetras) {
        listLetras = new ArrayList();
        String[] listaNumeros = listaLetras.split(",");
        for (int i = 0; i < listaNumeros.length; i++) { // no recuerdo si es length o length()
            listLetras.add(listaNumeros[i]);
        }
        treeString = Arbol.getArbol(Integer.parseInt(ordenArbol), new ComparadorString());

        for (String i : listLetras) {
            treeString.agregar(i);
        }
        System.out.println(treeString);
        this.orden.setText("");
        this.lista.setText("");
        this.panel.setText("");
        this.panel.append("\n");
        this.panel.append(treeString.toString());
    }

