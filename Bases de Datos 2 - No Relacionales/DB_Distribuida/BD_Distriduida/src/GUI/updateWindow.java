    private void btnInsCiudActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsCiudActionPerformed
        try {
            if (!txtCiudad.getText().isEmpty()) {
                dbserver.addCiudad(txtCiudad.getText());
                JOptionPane.showMessageDialog(null, "Ciudad Agregada", "CONFIRMACION", JOptionPane.INFORMATION_MESSAGE);
                txtCiudad.setText("");
            } else {
                JOptionPane.showMessageDialog(null, "Ingrese un nombre", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnInsCiudActionPerformed

    private void btnInsPerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsPerActionPerformed
        try {
            if (!txtnombre.getText().isEmpty() && !txtapellido.getText().isEmpty() && !txtid.getText().isEmpty()) {
                try {
                    int id = Integer.parseInt(txtid.getText());
                    try {
                        dbserver.addPersona(txtnombre.getText(), txtapellido.getText(), id);
                        JOptionPane.showMessageDialog(null, "Persona agregada", "CONFIRMACION", JOptionPane.INFORMATION_MESSAGE);
                        txtnombre.setText("");
                        txtapellido.setText("");
                        txtid.setText("");
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Ingrese un id de Ciudad numérico", "ERROR", JOptionPane.ERROR_MESSAGE);
                }

            } else {
                JOptionPane.showMessageDialog(null, "Rellene todos los campos", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnInsPerActionPerformed

    private void btnDelCiudActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelCiudActionPerformed
        int id = 0;
        if (!txtIDCiud.getText().isEmpty()) {
            try {
                id = Integer.parseInt(txtIDCiud.getText());
                try {
                    dbserver.delCiudad(id);
                    JOptionPane.showMessageDialog(null, "Ciudad Eliminada", "CONFIRMACION", JOptionPane.INFORMATION_MESSAGE);
                    txtIDCiud.setText("");
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Ingrese un id de Ciudad numérico", "ERROR", JOptionPane.ERROR_MESSAGE);
            }

        } else {
            JOptionPane.showMessageDialog(null, "Ingrese datos en el campo", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDelCiudActionPerformed

