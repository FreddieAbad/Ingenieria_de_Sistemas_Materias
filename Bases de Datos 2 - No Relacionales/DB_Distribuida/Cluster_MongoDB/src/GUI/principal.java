    /* CARGA LOS DATOS DE LA TABLA*/
    private void ponerTabla(ArrayList<ArrayList<String>> datos, JTable jTable) {
        DefaultTableModel tabla = (DefaultTableModel) jTable.getModel();
        for(int i = tabla.getRowCount() - 1; i >= 0 ; i--)
            tabla.removeRow(i);
        
        cabeceras = datos.get(0).toArray();
        DefaultTableModel nuevoModelo = new DefaultTableModel(cabeceras, 0);
        jTable.setModel(nuevoModelo);
        tabla = (DefaultTableModel) jTable.getModel();
        
        datos.remove(0);
        for (ArrayList<String> fila : datos) 
            tabla.addRow(fila.toArray());
        jTable.setDefaultEditor(Object.class, null);
        jTable.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }
    
    private ArrayList<String[]> getFilaSeleccionada() {
        ArrayList<String []> filaSeleccionada;
        int fila = jTableBusqueda.getSelectedRow();
        if (fila == -1) {
            filaSeleccionada = null;
        } else {
            filaSeleccionada = new ArrayList<>();
            String[] valor; 
            for (int columna = 0; columna < jTableBusqueda.getColumnCount(); columna++){
                valor = new String[2];
                valor[0] = jTableBusqueda.getColumnName(columna);
                if (jTableBusqueda.getValueAt(fila, columna) != null)
                    valor[1] = (String) jTableBusqueda.getValueAt(fila, columna);
                    
                filaSeleccionada.add(valor);
            }
        }
        return filaSeleccionada;
    }
    
    

