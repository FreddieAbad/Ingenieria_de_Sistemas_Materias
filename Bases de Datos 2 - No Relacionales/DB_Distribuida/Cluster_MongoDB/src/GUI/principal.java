    private void botonActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonActualizarActionPerformed
        try {
            ArrayList<String[]> valores = this.getFilaSeleccionada();
            if (valores != null) {
                Modificar modificar = new Modificar(null, true, valores);
                modificar.setLocationRelativeTo(null);
                modificar.setVisible(true);
                ArrayList<ArrayList<String>> resultado = dbserver.getPersons();
                ponerTabla(resultado, this.jTableBusqueda);
            } else {
                JOptionPane.showMessageDialog(null, "Seleccione una fila de la Tabla en 'INFORMACION DE TABLA'","ERROR",JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(),"ERROR",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_botonActualizarActionPerformed

    private void botonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarActionPerformed
        try {
            ArrayList<String []> filaSeleccionada = this.getFilaSeleccionada();
            if (filaSeleccionada != null) {
                if (JOptionPane.showConfirmDialog(null, "¿Está seguro de eliminar la fila?","CONFIRMACION",JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                    dbserver.deletePerson(filaSeleccionada);
                    JOptionPane.showMessageDialog(null, "Elemento eliminado","CONFIRMACION",JOptionPane.INFORMATION_MESSAGE);
                    ArrayList<ArrayList<String>> resultado = dbserver.getPersons();
                    ponerTabla(resultado, this.jTableBusqueda);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Seleccione una fila de la Tabla en 'INFORMACION DE TABLA'","ERROR",JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(),"ERROR",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_botonEliminarActionPerformed

