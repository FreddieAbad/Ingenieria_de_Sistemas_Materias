                for(int j = 0; j < i; j++){
                    if(this.jComboBox3.getSelectedItem().toString().equals(this.vPrincipal.nuevoGerente.getSucursales().get(m-1).getAdministradores().get(0).getGasolinera().get(0).getDispensadores().get(dispensador_numero-1).getEmpleados().get(j).getNombre())){
                        this.empleado = this.vPrincipal.nuevoGerente.getSucursales().get(m-1).getAdministradores().get(0).getGasolinera().get(0).getDispensadores().get(dispensador_numero-1).getEmpleados().get(j);
                    }
                }
                numerito = this.vPrincipal.nuevoGerente.getSucursales().get(m-1).getAdministradores().get(0).getGasolinera().get(0).getDispensadores().get(dispensador_numero-1).getVenta().size();
                
