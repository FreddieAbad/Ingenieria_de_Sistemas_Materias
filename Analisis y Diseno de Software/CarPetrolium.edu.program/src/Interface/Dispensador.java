                    this.clientito = new CustomerServiceStation(Integer.parseInt(this.jTextField14.getText()),this.jTextField3.getText(),this.jTextField4.getText(),this.jTextField5.getText(),this.jTextField2.getText(),this.vPrincipal.nuevoGerente.getSucursales().get(m-1).getAdministradores().get(0).getGasolinera().get(0));
