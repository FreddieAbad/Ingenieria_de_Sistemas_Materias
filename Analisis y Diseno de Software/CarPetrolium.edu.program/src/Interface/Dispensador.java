    private void aregarCombustibleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aregarCombustibleActionPerformed
      int y = Integer.parseInt(this.num.getText()); 

      if(y >= 4){
          JOptionPane.showMessageDialog(null,"LO SIENTO SOLO PUEDES INGRESAR HASTA 4 COMBUSTIBLES POR DISPENSADOR","ERROR",JOptionPane.ERROR_MESSAGE);
        }else{
           int p = Integer.parseInt(JOptionPane.showInputDialog(null, "INGRESE LA CEDULA DEL ADMINISTRADOR DE ESTA SUCURSAL\nO INGRESE LA CEDULA DEL GERENTE","ALTO!!! IDENTIFIQUESE",3));
        if(p == this.vPrincipal.nuevoGerente.getSucursales().get(m-1).getAdministradores().get(0).getRuc() ||  p == this.vPrincipal.nuevoGerente.getRuc()){
         
            if( jTextField6.getText().isEmpty()){
                JOptionPane.showMessageDialog(null,"EL NOMBRE DEL COMBUSTIBLE","ERROR",JOptionPane.ERROR_MESSAGE);
            }else if(jTextField7.getText().isEmpty() ){
                JOptionPane.showMessageDialog(null,"INGRESE EL COSTO POR GALON DEL PRODUCTO","ERROR",JOptionPane.ERROR_MESSAGE);
            }else if(jTextField9.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null,"INGRESE EL TOTAL DE GALONES QUE EXISTEN EN PRODUCCION","ERROR",JOptionPane.ERROR_MESSAGE);
            }else if(jComboBox2.getActionCommand().isEmpty() ){
                JOptionPane.showMessageDialog(null,"ELIJA UNA OPCION EN TIPO DE COMBUSTIBLE","ERROR",JOptionPane.ERROR_MESSAGE);
            }else{
                this.combustibleV = new Fuel(Integer.parseInt(jTextField9.getText()),Double.parseDouble(jTextField7.getText()),this.jComboBox2.getSelectedItem().toString(),jTextField6.getText(),this.vPrincipal.nuevoGerente.getSucursales().get(m-1).getAdministradores().get(0).getGasolinera().get(0).getDispensadores().get(dispensador_numero-1));
                this.vPrincipal.nuevoGerente.getSucursales().get(m-1).getAdministradores().get(0).getGasolinera().get(0).getDispensadores().get(dispensador_numero-1).createFuel(combustibleV);
                this.vPrincipal.nuevoGerente.getSucursales().get(m-1).getAdministradores().get(0).getGasolinera().get(0).getDispensadores().get(dispensador_numero-1).getCombustible().get(y).seeInfo();
                this.jComboBox1.addItem(this.combustibleV.toString());
                this.num.setText(String.valueOf(Integer.parseInt(this.num.getText()) + 1));
                this.modCombustible.setEnabled(false);
                limpiarFuel();
            }
        }else{
            JOptionPane.showMessageDialog(null,"LA CEDULA NO CORRESPONDE AL USUARIO ADMINSTRADOR DE ESTE BOTON","ERROR DE AUNTENTICIDAD",JOptionPane.ERROR_MESSAGE);
        }
      } 
    }//GEN-LAST:event_aregarCombustibleActionPerformed

