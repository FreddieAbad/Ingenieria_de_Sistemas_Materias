    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        limpiar();
        int m = Integer.parseInt(this.jTextField1.getText());
        if(m < 1 || m > this.vPrincial.nuevoGerente.getSucursales().size()){
                this.jTextField2.setText("NO SE A ENCONTRADO LA SUCURSAL VERIFIQUE EL CODIGO");
        }else{
             this.jTextField2.setText("SUCURSAL REGISTRADA COMO : " + this.vPrincial.nuevoGerente.getSucursales().get(m-1).getNombreSucursal());
            if(this.vPrincial.nuevoGerente.getSucursales().get(m-1).getAdministradores().size() == 0){
                this.registrar.setEnabled(true);
            }else{
                this.mod.setEnabled(true);
                this.jTextField6.setText(String.valueOf(this.vPrincial.nuevoGerente.getSucursales().get(m-1).getAdministradores().get(0).getRuc()));
                this.jTextField3.setText(this.vPrincial.nuevoGerente.getSucursales().get(m-1).getAdministradores().get(0).getNombre());
                this.jTextField4.setText(this.vPrincial.nuevoGerente.getSucursales().get(m-1).getAdministradores().get(0).getDireccion());
                this.jTextField5.setText(this.vPrincial.nuevoGerente.getSucursales().get(m-1).getAdministradores().get(0).getCorreo());
            }       
        } 
    }//GEN-LAST:event_jButton1ActionPerformed

