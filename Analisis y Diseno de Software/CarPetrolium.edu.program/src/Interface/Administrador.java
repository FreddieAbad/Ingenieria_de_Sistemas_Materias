    private void registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrarActionPerformed
        if( jTextField1.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Verifique la sucursal","ERROR",JOptionPane.ERROR_MESSAGE);
        }else if(jTextField3.getText().isEmpty() ){
            JOptionPane.showMessageDialog(null,"Ingrese el NOMBRE del ADMINISTRADOR","ERROR",JOptionPane.ERROR_MESSAGE);
        }else if(jTextField4.getText().isEmpty() ){
            JOptionPane.showMessageDialog(null,"Ingrese la DIRECCION del ADMINISTRADOR","ERROR",JOptionPane.ERROR_MESSAGE);
        }else if(jTextField5.getText().isEmpty() ){
            JOptionPane.showMessageDialog(null,"Ingrese el CORREO del ADMINISTRADOR","ERROR",JOptionPane.ERROR_MESSAGE);
        }else if(jTextField6.getText().isEmpty() ){
            JOptionPane.showMessageDialog(null,"Ingrese la CEDULA del ADMINISTRADOR","ERROR",JOptionPane.ERROR_MESSAGE);
        }else{
            int m = Integer.parseInt(this.jTextField1.getText());
            Administrator admin = new Administrator(Integer.parseInt(this.jTextField6.getText()),this.jTextField3.getText(),this.jTextField4.getText(),this.jTextField5.getText(),this.vPrincial.nuevoGerente.getSucursales().get(m-1));
            this.vPrincial.nuevoGerente.getSucursales().get(m-1).createAdministrator(admin);
            this.vPrincial.nuevoGerente.getSucursales().get(m-1).getAdministradores().get(0).seeInfo();
            limpiar();
            this.jTextField1.setText("");
            this.registrar.setEnabled(false);
            if(auxiliar == 1){
                auxiliar = 0;
                this.setVisible(false);
            }
        }
    }//GEN-LAST:event_registrarActionPerformed

    private void modActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modActionPerformed
        if( jTextField1.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Verifique la sucursal","ERROR",JOptionPane.ERROR_MESSAGE);
        }else if(jTextField3.getText().isEmpty() ){
            JOptionPane.showMessageDialog(null,"Ingrese el NOMBRE del ADMINISTRADOR","ERROR",JOptionPane.ERROR_MESSAGE);
        }else if(jTextField4.getText().isEmpty() ){
            JOptionPane.showMessageDialog(null,"Ingrese la DIRECCION del ADMINISTRADOR","ERROR",JOptionPane.ERROR_MESSAGE);
        }else if(jTextField5.getText().isEmpty() ){
            JOptionPane.showMessageDialog(null,"Ingrese el CORREO del ADMINISTRADOR","ERROR",JOptionPane.ERROR_MESSAGE);
        }else if(jTextField6.getText().isEmpty() ){
            JOptionPane.showMessageDialog(null,"Ingrese la CEDULA del ADMINISTRADOR","ERROR",JOptionPane.ERROR_MESSAGE);
        }else{
            int m = Integer.parseInt(this.jTextField1.getText());
            this.vPrincial.nuevoGerente.getSucursales().get(m-1).getAdministradores().get(0).setRuc(Integer.parseInt(this.jTextField6.getText()));
            this.vPrincial.nuevoGerente.getSucursales().get(m-1).getAdministradores().get(0).setNombre(this.jTextField3.getText());
            this.vPrincial.nuevoGerente.getSucursales().get(m-1).getAdministradores().get(0).setDireccion(this.jTextField4.getText());
            this.vPrincial.nuevoGerente.getSucursales().get(m-1).getAdministradores().get(0).setCorreo(this.jTextField5.getText());
            this.vPrincial.nuevoGerente.getSucursales().get(m-1).getAdministradores().get(0).seeInfo();
            limpiar();
            this.jTextField1.setText("");
            this.mod.setEnabled(false);
        } 
    }//GEN-LAST:event_modActionPerformed

